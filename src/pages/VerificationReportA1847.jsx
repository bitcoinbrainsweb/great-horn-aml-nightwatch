const RESULT_CONFIG = {
  PASS: { icon: CheckCircle2, color: 'text-emerald-600', bg: 'bg-emerald-50 border-emerald-200', label: 'PASS' },
  WARN: { icon: AlertTriangle, color: 'text-amber-500', bg: 'bg-amber-50 border-amber-200', label: 'WARNING' },
  FAIL: { icon: XCircle, color: 'text-red-500', bg: 'bg-red-50 border-red-200', label: 'FAIL' },
};

function ResultBadge({ result }) {
  const cfg = RESULT_CONFIG[result] || RESULT_CONFIG.WARN;
  const Icon = cfg.icon;
  return (
    <span className={`inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-bold border ${cfg.bg} ${cfg.color}`}>
      <Icon className="w-3 h-3" />
      {cfg.label}
    </span>
  );
}

function SectionRow({ section, checks }) {
  const [open, setOpen] = useState(false);
  const pass = checks.filter(c => c.result === 'PASS').length;
  const warn = checks.filter(c => c.result === 'WARN').length;
  const fail = checks.filter(c => c.result === 'FAIL').length;
  const overall = fail > 2 ? 'FAIL' : warn > 0 || fail > 0 ? 'WARN' : 'PASS';

  return (
    <div className="border border-slate-200 rounded-xl overflow-hidden">
      <button
        onClick={() => setOpen(!open)}
        className="w-full flex items-center justify-between px-5 py-4 bg-white hover:bg-slate-50 transition-colors text-left"
      >
        <div className="flex items-center gap-3">
          {open ? <ChevronDown className="w-4 h-4 text-slate-400" /> : <ChevronRight className="w-4 h-4 text-slate-400" />}
          <span className="text-sm font-semibold text-slate-900">{section}</span>
        </div>
        <div className="flex items-center gap-2">
          {pass > 0 && <span className="text-xs font-bold text-emerald-600 bg-emerald-50 border border-emerald-200 px-2 py-0.5 rounded-full">{pass} PASS</span>}
          {warn > 0 && <span className="text-xs font-bold text-amber-600 bg-amber-50 border border-amber-200 px-2 py-0.5 rounded-full">{warn} WARN</span>}
          {fail > 0 && <span className="text-xs font-bold text-red-600 bg-red-50 border border-red-200 px-2 py-0.5 rounded-full">{fail} FAIL</span>}
        </div>
      </button>
      {open && (
        <div className="border-t border-slate-100 divide-y divide-slate-100">
          {checks.map(c => (
            <div key={c.n} className="px-5 py-3 flex items-start gap-3">
              <span className="text-xs text-slate-400 w-5 flex-shrink-0 mt-0.5 font-mono">{c.n}</span>
              <div className="flex-1 min-w-0">
                <p className="text-sm font-medium text-slate-900">{c.label}</p>
                <p className="text-xs text-slate-500 mt-0.5"><span className="font-medium text-slate-600">Where:</span> {c.where}</p>
                <p className="text-xs text-slate-600 mt-0.5">{c.notes}</p>
              </div>
              <ResultBadge result={c.result} />
            </div>
          ))}
        </div>
      )}
    </div>
  );
}

